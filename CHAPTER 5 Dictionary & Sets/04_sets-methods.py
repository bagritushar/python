
s = {1, 2, 3, 4, 5}
print(s)    

s.add(6)
print(s)
s.remove(3) # if element not found then it will raise an error
print(s)    
s.discard(10) # if element not found then it will not raise an error
print(s)    
s.pop() # removes a random element
print(s)
s.clear() # removes all elements
print(s)
print(len(s))

